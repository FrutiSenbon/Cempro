<?php 
include_once("../../conexion/Conexion.class.php");

class Cliente{
 //constructor	
 	var $con;
 	function Cliente(){
 		$this->con=new Conexion;
	}
	function armarConsulta($condicion,$papelera){
		if ($condicion!=""){
			if($papelera){
				$consulta="WHERE ((clientes.nombre LIKE '%".$condicion."%') OR (clientes.telefono LIKE '%".$condicion."%') OR (clientes.correo LIKE '%".$condicion."%'))AND clientes.estatus ='eliminado'";
			}else{
				$consulta="WHERE ((clientes.nombre LIKE '%".$condicion."%') OR (clientes.telefono LIKE '%".$condicion."%') OR (clientes.correo LIKE '%".$condicion."%'))AND clientes.estatus <>'eliminado'";
			}
		}else{
			if($papelera){
				$consulta="WHERE clientes.estatus ='eliminado'";
			}else{
				$consulta="WHERE clientes.estatus <>'eliminado'";
			}
		}
		return $consulta;
	}
	function comprobarCampo($campo, $valor, $tipoGuardado){
		if($this->con->conectar()==true){
			//print_r($listaCampos);
			$resultado=mysqli_query($this->con->conect,"SELECT COUNT( * ) AS contador from clientes WHERE $campo = '$valor'");
			if ($resultado){
				$extractor = mysqli_fetch_array($resultado);
				$numero_filas=$extractor["contador"];
				if ($tipoGuardado=='nuevo'){
					if ($numero_filas=="0"){
						return false;
					}else{
						return true;
					}
				}
				if ($tipoGuardado=='modificar'){
					if ($numero_filas=="1" or $numero_filas=="0"){
						return false;
					}else{
						return true;
					}
				}
			}else{
				return false;
			}
		}
	}

	function guardar($nombre,$telefono,$correo,$estatus){
		/////PERMISOS////////////////
		if (!isset($_SESSION['permisos']['clientes']['guardar'])){
			return "denegado";
			exit;
		}
		/////FIN  DE PERMISOS////////
		
		$idCliente=$this->con->generarClave(2); /*Sincronizacion 1 */
		
		if($this->con->conectar()==true){
			if($this->comprobarCampo("nombre",$nombre, "nuevo")){
				return "nombreExiste";
			}else{
				if(mysqli_query($this->con->conect,"INSERT INTO clientes (idCliente, nombre, telefono, correo, estatus) VALUES ('$idCliente','$nombre','$telefono','$correo','$estatus')")){
					//BITACORA
					if ($_SESSION['bitacora']=="si"){
						$descripcionB="agreg&oacute; un nuevo registro en la tabla clientes ";
						$this->registrarBitacora("guardar",$descripcionB);
					}
					return "exito";
				}else{
					return "fracaso";
				}
			}
		}
	}
	
	function actualizar($nombre,$telefono,$correo,$estatus,$idCliente){
		/////PERMISOS////////////////
		if (!isset($_SESSION['permisos']['clientes']['modificar'])){
			return "denegado";
			exit;
		}
		/////FIN  DE PERMISOS////////
		
		if($this->con->conectar()==true){
			if($this->comprobarCampo("nombre",$nombre, "modificar")){
				return "nombreExiste";
			}else{
				if(mysqli_query($this->con->conect,"UPDATE clientes SET nombre='$nombre', telefono='$telefono', correo='$correo', estatus='$estatus' WHERE idCliente='$idCliente'")){
					//BITACORA
					if ($_SESSION['bitacora']=="si"){
						$descripcionB="modific&oacute; el registro ID: $idCliente, de la tabla clientes ";
						$this->registrarBitacora("modificar",$descripcionB);
					}
					return "exito";
				}else{
					return "fracaso";
				}
			}
		}
	}
	
